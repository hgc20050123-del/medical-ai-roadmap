# 2.变量的命名
# 变量命名的规范
# 1.只能包含字母、数字和下划线，不能以数字开头
hg_c = 123
hgc123 = hg_c
print(hgc123)
# 2.不能使用Python的关键字作为变量名
# 3.变量名应该具有描述性，能够清晰地表达变量的含义
patient_name = "老王"
patient_age = 45 #岁
patient_gender = "女" 
patient_height = 150 # cm
patient_weight = 50  # kg
print("患者姓名:", patient_name)
print("患者年龄：",patient_age,"岁")
print("患者性别：",patient_gender)
print("患者体重：",patient_weight,"kg")
print("身高:",patient_height,"cm")
